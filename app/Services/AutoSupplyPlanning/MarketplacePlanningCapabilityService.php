    /**
     * @return array<string, mixed>
     */
    public function forMarketplace(string $marketplace): array
    {
        $marketplace = $this->normalizeMarketplace($marketplace);

        return match ($marketplace) {
            'ozon' => $this->ozon(),
            'wildberries' => $this->wildberries(),
            'yandex_market' => $this->yandexMarket(),
            default => $this->generic($marketplace),
        };
    }

    private function normalizeMarketplace(string $marketplace): string
    {
        return match ($marketplace) {
            'yandex' => 'yandex_market',
            default => $marketplace,
        };
    }

